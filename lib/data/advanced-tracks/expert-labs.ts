import type { Lab, LabLevel, LabTechnology } from "@/lib/types";

function expertLab(
  slug: string,
  title: string,
  description: string,
  level: LabLevel,
  duration: string,
  technology: LabTechnology,
  trackSlug: string,
  scenario: string,
  objectives: string[],
  prerequisites: string[],
  steps: Lab["steps"],
  expectedResult: string
): Lab {
  return {
    slug,
    title,
    description: `${description} Scénario entreprise : ${scenario}`,
    level,
    duration,
    technology,
    trackSlug,
    objectives,
    prerequisites,
    steps,
    expectedResult,
    objective: `${objectives[0] ?? description} — scénario enterprise pilote : ${scenario.slice(0, 70)}`,
  };
}

const checklistStep = (id: string, title: string, items: string[]) => ({
  id,
  title,
  instruction: `Checklist validation :\n${items.map((i) => `• ${i}`).join("\n")}`,
  expectedResult: "Tous les points de la checklist sont cochés et documentés.",
});

